extension String {
    // Extract the code inside parentheses, e.g. "Athens (ATH)" -> "ATH"
    var airportCodeInParens: String? {
        guard let start = firstIndex(of: "("),
              let end = firstIndex(of: ")"),
              start < end else { return nil }
        let code = self[self.index(after: start)..<end]
        return String(code)
    }
}
